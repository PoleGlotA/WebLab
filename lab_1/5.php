<?php

function findMinDifference($A, $B) {
    // Знаходимо мінімальний елемент у масиві A
    $minA = min($A);
    
    // Знаходимо мінімальний елемент у масиві B
    $minB = min($B);
    
    // Знаходимо різницю між мінімальними елементами
    $difference = $minA - $minB;
    
    return $difference;
}

// Приклад вхідних масивів
$A = [5, 9, 3, 8, 4, 6, 7];
$B = [2, 4, 7, 5, 3, 9, 6];

// Знаходимо різницю між мінімальними елементами масивів A та B
$difference = findMinDifference($A, $B);

echo "Різниця між мінімальними елементами масивів A та B: $difference";

?>